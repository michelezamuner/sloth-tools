module.exports = [
  // Memory layout:
  // &MEM[0x00] - &MEM[0x05]: address list
  // &MEM[0x06] - &MEM[0x19]: firmware
  // &MEM[0x1A] - &MEM[0x1D]: device table
  // &MEM[0x1E]: free memory

  // initialize address list in memory
  // &MEM[0x00]: firmware address
  // &MEM[0x02]: device table address
  // &MEM[0x04]: usable memory address

  // set firmware address, which is always &MEM[0x06],
  // which is the first address after the address list
  /* 0x00 */ 0x01, 0x00, 0x02, 0x06,  // MOV R0 &MEM[0x06]
  /* 0x04 */ 0x05, 0x00, 0x02, 0x00,  // WRITE R0 &MEM[0x00]

  // set device table address, which is always &MEM[0x1A]
  /* 0x08 */ 0x01, 0x00, 0x02, 0x1A,  // MOV R0 &MEM[0x1A]
  /* 0x0C */ 0x05, 0x00, 0x02, 0x02,  // WRITE RO &MEM[0x02]

  // shutdown
  /* 0x10 */ 0x01, 0x00, 0x00, 0x00,  // MOV R0 PROC_SHUTDOWN
  /* 0x14 */ 0x05, 0x00, 0x01, 0x00,  // WRITE R0 &PROC[0x00]
];
